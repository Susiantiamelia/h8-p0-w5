function highestScore (students) {
  var result= {}
    
  for(var a=0; a < students.length; a++){

    var cekNilai = 0

    for(var b=0; b < students.length; b++){
     
      if(students[a].class === students[b].class){

        if(students[a].score > cekNilai){

          result[students[a].class] = {}

          cekNilai = students[b].score

          result[students[a].class].name= students[b].name
          result[students[a].class].score= students[b].score
        }
      }
    } 
  }
  
  return result;  
  
}


